<?php

namespace App\Http\Controllers;

use App\Models\Employe;
use App\Models\Journal;
use Illuminate\Http\Request;

class JournalController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $employes  = Employe::with('journal')->orderBy('id','ASC');
        $journals = Journal::All();
        return view('journal.index', compact('employes', 'journals' ));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $employes = Employe::All();
        $journals = Journal::All();
        return view ('journal.create', compact('employes', 'journals'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'nom'=> 'required|exists:employes,id',


        ]);

        $journal = new Journal();

        $journal->employe_id = $request->nom;

        $journal->save();
        return redirect()->route('journal.index')->with('message', 'le journal de connexion a été ajouté avec succès');
    }

